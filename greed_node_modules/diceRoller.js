function resetJustRolledAndDeselected(dice) {
  dice.forEach(function(die) {
    die.justRolled = false;
    die.deselected = undefined;
  });
}

function checkAllNotInPlay(dice) {
  var count = 0;
  dice.forEach(function(die) {
    if(!die.inPlay) {
      count++;
    }
  });
  if (count === 6) {
    dice.forEach(function(die) {
      die.inPlay = true;
      die.win = undefined;
      die.score = undefined;
    });
  }
}

function rollDiceInPlay(dice) {
  dice.forEach(function(die){
    if (die.inPlay) {
      die.value = Math.floor(Math.random() * 6) + 1;
      die.justRolled = true;
    }
  });

  dice[0].value = 5;
  dice[1].value = 6;
  dice[2].value = 6;
  dice[3].value = 6;
  dice[4].value = 3;
  dice[5].value = 2;
  dice[0].justRolled = true;
  dice[1].justRolled = true;
  dice[2].justRolled = true;
  dice[3].justRolled = true;
  dice[4].justRolled = true;
  dice[5].justRolled = true;
}

module.exports.resetJustRolledAndDeselected = resetJustRolledAndDeselected;
module.exports.rollDiceInPlay = rollDiceInPlay;
module.exports.checkAllNotInPlay = checkAllNotInPlay;